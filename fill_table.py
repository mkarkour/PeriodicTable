def insert_element(nombre_atomique,name,symbole,masse_atomique,electronegativite,electron_orbitale,electron_couche):
    dbase = sqlite3.connect('mendeleev.db', isolation_level=None)
    dbase.execute("PRAGMA foreign_keys = 1")
    dbase.execute('''INSERT INTO Element(Nombre_atomique,Name, Symbole, Masse_atomique, Electronegativite, Electron_orbitale,Electron_couche)
            VALUES(?,?,?,?,?,?,?)
                ''', (nombre_atomique,name,symbole, masse_atomique, electronegativite, electron_orbitale,electron_couche)
                  )
    dbase.close()
    print(str(name) + " added to the database")

